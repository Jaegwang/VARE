// http://paulboure.net/geometry/polygonise
// https://en.wikipedia.org/wiki/Marching_cubes
//
//       @--------------@
//      /|4            /|5
//     / |            / |
//    @--------------@  |
//    |7 |           |6 |
//    |  |           |  |
//    |  @-----------|--@
//    | /0           | /1
//    |/             |/
//    @--------------@
//    3              2  

void
MarchingCubes::initialize()
{
    if( _pEdgeTable == 0  )
    {
        cudaMallocManaged( (void**)(&_pEdgeTable), sizeof(int)*256 );

        for( int n=0; n<256; ++n ) _pEdgeTable[n] = _edgeTable[n];
    }

    if( _pTriTable == 0 )
    {
        cudaMallocManaged( (void***)(&_pTriTable), sizeof(int*)*256 );

        for( int n=0; n<256; ++n )
        {
            cudaMallocManaged( (void**)(&_pTriTable[n]), sizeof(int)*16 );

            for( int i=0; i<16; ++i )
            {
                _pTriTable[n][i] = _triTable[n][i];
            }
        }
    }
}

Vec3f
MarchingCubes::vertexInterpolate( const Vec3f& p0, const Vec3f& p1, const float& v0, const float& v1, const float isoValue )
{
    return p0 + (p1-p0)*( isoValue-v0 )/(v1-v0);
}

void
MarchingCubes::march( const FloatSparseField& field, Vec3fArray& vertices )
{
    IndexArray vertCounts( field.size(), kUnified );
    vertCounts.zeroize();

    for( size_t n=0; n<field.size(); ++n )
    {
        Idx3 idx = field.findIndices( n );

        if( idx.i >= field.nx()-1 || idx.j >= field.ny()-1 || idx.k >= field.nz()-1 ) continue;

        const Vec3f P[8] =
        {
            field.cellCenter( idx.i  , idx.j  , idx.k   ),
            field.cellCenter( idx.i+1, idx.j  , idx.k   ),
            field.cellCenter( idx.i+1, idx.j  , idx.k+1 ),
            field.cellCenter( idx.i  , idx.j  , idx.k+1 ),
            field.cellCenter( idx.i  , idx.j+1, idx.k   ),
            field.cellCenter( idx.i+1, idx.j+1, idx.k   ),
            field.cellCenter( idx.i+1, idx.j+1, idx.k+1 ),
            field.cellCenter( idx.i  , idx.j+1, idx.k+1 )
        };        

        const float V[8] =
        {
            field( idx.i  , idx.j  , idx.k   ),
            field( idx.i+1, idx.j  , idx.k   ),
            field( idx.i+1, idx.j  , idx.k+1 ),
            field( idx.i  , idx.j  , idx.k+1 ),
            field( idx.i  , idx.j+1, idx.k   ),
            field( idx.i+1, idx.j+1, idx.k   ),
            field( idx.i+1, idx.j+1, idx.k+1 ),
            field( idx.i  , idx.j+1, idx.k+1 )
        };

        int id(0);
        if( V[0] < isoValue ) id |= 1;
        if( V[1] < isoValue ) id |= 2;
        if( V[2] < isoValue ) id |= 4;
        if( V[3] < isoValue ) id |= 8;
        if( V[4] < isoValue ) id |= 16;
        if( V[5] < isoValue ) id |= 32;
        if( V[6] < isoValue ) id |= 64;
        if( V[7] < isoValue ) id |= 128;

        const int& et = _pEdgeTable[id];
        if( et == 0   ) continue;

        int nTri = 0;
        for( int t=0; _triTable[id][t]!=-1; t+=3 )
        {
            nTri++;
        }

        vertCounts[n] = nTri*3;
    }

    size_t ptr(0);
    for( size_t n=0; n<vertCounts.size(); ++n )
    {
        if( vertCounts[n] > 0 )
        {
            vertCounts[n] = ptr;
        }
        else
        {
            vertCounts[n] = NULL_MAX;
        }

        ptr += vertCounts[n];
    }

    vertices.initialize( ptr, kUnified );
    vertices.clear();


    for( size_t n=0; n<field.size(); ++n )
    {
        Idx3 idx = field.findIndices( n );

        if( idx.i >= field.nx()-1 || idx.j >= field.ny()-1 || idx.k >= field.nz()-1 ) continue;

        const Vec3f P[8] =
        {
            field.cellCenter( idx.i  , idx.j  , idx.k   ),
            field.cellCenter( idx.i+1, idx.j  , idx.k   ),
            field.cellCenter( idx.i+1, idx.j  , idx.k+1 ),
            field.cellCenter( idx.i  , idx.j  , idx.k+1 ),
            field.cellCenter( idx.i  , idx.j+1, idx.k   ),
            field.cellCenter( idx.i+1, idx.j+1, idx.k   ),
            field.cellCenter( idx.i+1, idx.j+1, idx.k+1 ),
            field.cellCenter( idx.i  , idx.j+1, idx.k+1 )
        };        

        const float V[8] =
        {
            field( idx.i  , idx.j  , idx.k   ),
            field( idx.i+1, idx.j  , idx.k   ),
            field( idx.i+1, idx.j  , idx.k+1 ),
            field( idx.i  , idx.j  , idx.k+1 ),
            field( idx.i  , idx.j+1, idx.k   ),
            field( idx.i+1, idx.j+1, idx.k   ),
            field( idx.i+1, idx.j+1, idx.k+1 ),
            field( idx.i  , idx.j+1, idx.k+1 )
        };

        int id(0);
        if( V[0] < isoValue ) id |= 1;
        if( V[1] < isoValue ) id |= 2;
        if( V[2] < isoValue ) id |= 4;
        if( V[3] < isoValue ) id |= 8;
        if( V[4] < isoValue ) id |= 16;
        if( V[5] < isoValue ) id |= 32;
        if( V[6] < isoValue ) id |= 64;
        if( V[7] < isoValue ) id |= 128;

        const int& et = _pEdgeTable[id];
        Vec3f vertList[12];

        if( et == 0   ) continue;
        if( et & 1    ) vertList[0]  = vertexInterpolate( P[0], P[1], V[0], V[1] );
        if( et & 2    ) vertList[1]  = vertexInterpolate( P[1], P[2], V[1], V[2] );
        if( et & 4    ) vertList[2]  = vertexInterpolate( P[2], P[3], V[2], V[3] );
        if( et & 8    ) vertList[3]  = vertexInterpolate( P[3], P[0], V[3], V[0] );
        if( et & 16   ) vertList[4]  = vertexInterpolate( P[4], P[5], V[4], V[5] );
        if( et & 32   ) vertList[5]  = vertexInterpolate( P[5], P[6], V[5], V[6] );
        if( et & 64   ) vertList[6]  = vertexInterpolate( P[6], P[7], V[6], V[7] );
        if( et & 128  ) vertList[7]  = vertexInterpolate( P[7], P[4], V[7], V[4] );
        if( et & 256  ) vertList[8]  = vertexInterpolate( P[0], P[4], V[0], V[4] );
        if( et & 512  ) vertList[9]  = vertexInterpolate( P[1], P[5], V[1], V[5] );
        if( et & 1024 ) vertList[10] = vertexInterpolate( P[2], P[6], V[2], V[6] );
        if( et & 2048 ) vertList[11] = vertexInterpolate( P[3], P[7], V[3], V[7] );

        size_t ptr = vertCounts[n];
        size_t count(0);
        
        for( int t=0; _triTable[id][t]!=-1; t+=3 )
        {
            const Vec3f& v0 = vertList[ _pTriTable[id][t+0] ];
            const Vec3f& v1 = vertList[ _pTriTable[id][t+1] ];
            const Vec3f& v2 = vertList[ _pTriTable[id][t+2] ];

            //vertices.append( v0 );
            //vertices.append( v1 );
            //vertices.append( v2 );

            vertices[ ptr+count+0 ] = v0;
            vertices[ ptr+count+1 ] = v1;
            vertices[ ptr+count+2 ] = v2;

            count += 3;
        }
    }
}

void
MarchingCubes::march( const ScalarDenseField& field, Vec3fArray& vertices )
{
    vertices.clear();
    
    for( int i=0; i<field.nx()-1; ++i )
    for( int j=0; j<field.ny()-1; ++j )
    for( int k=0; k<field.nz()-1; ++k )
    {
        const Vec3f P[8] =
        {
            field.cellCenter( i,j,k       ), 
            field.cellCenter( i+1,j,k     ),
            field.cellCenter( i+1,j,k+1   ),
            field.cellCenter( i,j,k+1     ),
            field.cellCenter( i,j+1,k     ),
            field.cellCenter( i+1,j+1,k   ),
            field.cellCenter( i+1,j+1,k+1 ),
            field.cellCenter( i,j+1,k+1   )
        };

        const float V[8] = 
        {
            field( i,j,k       ), 
            field( i+1,j,k     ),
            field( i+1,j,k+1   ),
            field( i,j,k+1     ),
            field( i,j+1,k     ),
            field( i+1,j+1,k   ),
            field( i+1,j+1,k+1 ),
            field( i,j+1,k+1   )
        };

        int idx(0);
        if( V[0] < isoValue ) idx |= 1;
        if( V[1] < isoValue ) idx |= 2;
        if( V[2] < isoValue ) idx |= 4;
        if( V[3] < isoValue ) idx |= 8;
        if( V[4] < isoValue ) idx |= 16;
        if( V[5] < isoValue ) idx |= 32;
        if( V[6] < isoValue ) idx |= 64;
        if( V[7] < isoValue ) idx |= 128;

        const int& et = _edgeTable[idx];
        Vec3f vertList[12];

        if( et == 0   ) continue;
        if( et & 1    ) vertList[0]  = vertexInterpolate( P[0], P[1], V[0], V[1] );
        if( et & 2    ) vertList[1]  = vertexInterpolate( P[1], P[2], V[1], V[2] );
        if( et & 4    ) vertList[2]  = vertexInterpolate( P[2], P[3], V[2], V[3] );
        if( et & 8    ) vertList[3]  = vertexInterpolate( P[3], P[0], V[3], V[0] );
        if( et & 16   ) vertList[4]  = vertexInterpolate( P[4], P[5], V[4], V[5] );
        if( et & 32   ) vertList[5]  = vertexInterpolate( P[5], P[6], V[5], V[6] );
        if( et & 64   ) vertList[6]  = vertexInterpolate( P[6], P[7], V[6], V[7] );
        if( et & 128  ) vertList[7]  = vertexInterpolate( P[7], P[4], V[7], V[4] );
        if( et & 256  ) vertList[8]  = vertexInterpolate( P[0], P[4], V[0], V[4] );
        if( et & 512  ) vertList[9]  = vertexInterpolate( P[1], P[5], V[1], V[5] );
        if( et & 1024 ) vertList[10] = vertexInterpolate( P[2], P[6], V[2], V[6] );
        if( et & 2048 ) vertList[11] = vertexInterpolate( P[3], P[7], V[3], V[7] );

        int nTri = 0;
        for( int t=0; _triTable[idx][t]!=-1; t+=3 )
        {
            const Vec3f& v0 = vertList[ _triTable[idx][t+0] ];
            const Vec3f& v1 = vertList[ _triTable[idx][t+1] ];
            const Vec3f& v2 = vertList[ _triTable[idx][t+2] ];

            vertices.append( v0 );
            vertices.append( v1 );
            vertices.append( v2 );

            nTri++;
        }
        
    }
}

